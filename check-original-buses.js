require('dotenv').config();
const { PrismaClient } = require('@prisma/client');
const prisma = new PrismaClient();

async function checkOriginalBuses() {
  console.log('🔍 Analyzing bus data...\n');

  try {
    // Get all buses
    const allBuses = await prisma.transporte.findMany({
      select: {
        id: true,
        matricula: true,
        codigo: true,
        viaId: true
      },
      orderBy: {
        codigo: 'asc'
      }
    });

    console.log(`📊 Total buses in database: ${allBuses.length}\n`);

    // Categorize buses
    const reverseBuses = allBuses.filter(b => b.matricula.includes('-R'));
    const varBuses = allBuses.filter(b => b.matricula.includes('-VAR') && !b.matricula.includes('-R'));
    const generatedBuses = allBuses.filter(b => 
      (b.matricula.startsWith('VIA-') || b.matricula.startsWith('BUS-')) && 
      !b.matricula.includes('-R')
    );
    
    // Original buses (not generated by scripts)
    const originalBuses = allBuses.filter(b => 
      !b.matricula.includes('-R') && 
      !b.matricula.includes('-VAR') && 
      !b.matricula.startsWith('VIA-') && 
      !b.matricula.startsWith('BUS-')
    );

    console.log('📋 Bus categories:');
    console.log(`   - Reverse buses (with -R): ${reverseBuses.length}`);
    console.log(`   - VAR buses (variants): ${varBuses.length}`);
    console.log(`   - Generated buses (VIA-*, BUS-*): ${generatedBuses.length}`);
    console.log(`   - Original buses: ${originalBuses.length}`);

    console.log('\n📝 Sample of each category:');
    
    console.log('\n   Original buses (first 10):');
    originalBuses.slice(0, 10).forEach(b => {
      console.log(`      ${b.matricula} (codigo: ${b.codigo})`);
    });

    console.log('\n   VAR buses (first 10):');
    varBuses.slice(0, 10).forEach(b => {
      console.log(`      ${b.matricula} (codigo: ${b.codigo})`);
    });

    console.log('\n   Generated buses (first 10):');
    generatedBuses.slice(0, 10).forEach(b => {
      console.log(`      ${b.matricula} (codigo: ${b.codigo})`);
    });

    console.log('\n   Reverse buses (first 10):');
    reverseBuses.slice(0, 10).forEach(b => {
      console.log(`      ${b.matricula} (codigo: ${b.codigo})`);
    });

    // Check which buses to keep
    console.log('\n💡 Recommendation:');
    console.log(`   Keep: ${originalBuses.length + varBuses.length} buses (original + VAR)`);
    console.log(`   Delete: ${reverseBuses.length + generatedBuses.length} buses (reverse + generated)`);

  } catch (error) {
    console.error('❌ Error:', error);
  } finally {
    await prisma.$disconnect();
  }
}

checkOriginalBuses();
